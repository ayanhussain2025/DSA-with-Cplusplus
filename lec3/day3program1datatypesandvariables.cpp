#include <iostream>
using namespace std;

int main()
{
    //variable declarations 4bytes
    int a;
    //variable define
    a = 10;
    //variable declartions and define
    int b = 20;
    cout<<a<<endl<<b<<endl<<"the sum of a and b : "<<a+b<<endl;

    //character 1bytes
    char c ='e';
    cout<<c<<endl;

    //float 4bytes

    float f = 2.31;
    cout<<f<<endl;

    //double 8bytes

    double d = 3.121;
    cout<<d<<endl; 

    //boolean

    bool g = 0;
    bool h = 1;
    bool i = true ;
    bool j = false ;
    cout<<g<<endl<<h<<endl<<i<<endl<<j<<endl;
    return 0;
    
}